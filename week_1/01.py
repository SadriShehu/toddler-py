# Variabla e tipit string
varString = 'Sadri'
print(varString)
print(type(varString))

# Variabla e tipit int
varInt = 7
print(varInt)
print(type(varInt))

# Variabla e tipit bool
varBool = True
print(varBool)
print(type(varBool))

# Variabla e tipit float
varFloat = 1.1
print(varFloat)
print(type(varFloat))

# Variabla e tipit tuple
varTuple = (1,2,3)
print(varTuple)
print(type(varTuple))

# Variabla e tipit list
varList = ["Sadri", 1, True, [1,2,3], (1,2,3)]
print(varList)
print(type(varList))

# Variabla e tipit dict
varDict = {
    "key": "value",
    1: (1,2,3),
    "list": [1,2,3],
    "dict": {
        1: 2
    }
}
print(varDict)
print(type(varDict))